public class Vehicle {
    int id;
    String type;
    String model;
    int year;
    int price;
    boolean isBooked;

    public Vehicle(int id, String type, String model, int year, int price, boolean isBooked) {
        this.id = id;
        this.type = type;
        this.model = model;
        this.year = year;
        this.price = price;
        this.isBooked = isBooked;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public boolean isBooked() {
        return isBooked;
    }

    public void setBooked(boolean booked) {
        isBooked = booked;
    }
}